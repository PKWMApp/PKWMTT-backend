package org.pkwmtt.timetable.enums;

/**
 * Represents the type of week for scheduling purposes.
 * This enum defines three possible values:
 * - ODD: Represents odd weeks.
 * - EVEN: Represents even weeks.
 * - BOTH: Represents both odd and even weeks.
 */
public enum TypeOfWeek {
    ODD, EVEN, BOTH
}

